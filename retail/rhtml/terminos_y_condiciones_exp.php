            fallida. Se le podrán ofrecer al Destinatario opciones de entrega
            alternativas, tales como entrega otro día, entrega sin necesidad de firma,
            redirección del envío o recogida en un Punto de Venta de GOLD COAST. El
            Remitente podrá descartar ciertas opciones de entrega bajo petición.
